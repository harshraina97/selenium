class ArithmaticOp{
	public static void main(String[] args) {
		int num1=10;
		int num2=20;
		int res1=num1+num2;
		System.out.println("Add: "+res1);
		int res2=num1-num2;
		System.out.println("Sub: "+res2);
		int res3=num1*num2;
		System.out.println("Mul: "+res3);
		int res4=num1%num2;
		System.out.println("Div: "+res4);
		int res5=num1/num2;
		System.out.println("Rem: "+res5);
	}
}